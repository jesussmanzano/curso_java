package prueba;

public class Suma {
	
	// Sobre escritura de metodos
	
	public void suma(int a, int b) {		
		System.out.println("Suma de numeros enteros"+ (a+b));
	}
	
	public void suma(float a, float b) {
		System.out.println("Suma de numeros flotantes " +(a+b));
	}
	
	public void suma(int a, int b, int c) {
		System.out.println("Suma de numeros enteros "+ (a+b+c));
	}
}
